        cen = obs[(fam, "content")]
        agree = "agrees" if (o > 0) == (cen > 0) and p2 < 0.05 else (
            "agrees (sign)" if (o > 0) == (cen > 0) else "DISAGREES")
        emit("- {}: instance-level effect {:+.5f} (p_2 {:.4f}) vs centroid "
             "{:+.5f} -> {}.".format(fam, o, p2, cen, agree))
        CHECK_ROWS.append({"check": "instance_content", "key": fam,
                           "value": round(float(o), 6),
                           "value2": round(p2, 6), "n": ""})
